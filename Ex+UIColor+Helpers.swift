import Foundation
import UIKit
extension UIColor {
    class func blueDark () -> UIColor{
        return UIColor(red: 16.0/255.0, green: 12.0/255.0, blue: 66.0/255.0, alpha: 1.0)
    }
    class func blackOff() -> UIColor{
        return UIColor(red: 27.0/255.0, green: 27.0/255.0, blue: 27.0/255.0, alpha: 1.0)
    }
}
